const WORK_WEEKEND_LABEL = '调休补班';

/**
 * @returns {{ labels: string[], primary: string|null, solarTerm: string|null, isBirthday: boolean }}
 */
export function getDayMarks(key) {
  const labels = [];
  let solarTerm = SOLAR_TERMS[key] || null;
  if (solarTerm) labels.push(solarTerm);

  const named = NAMED_DAYS[key];
  if (named) labels.push(...named);

  const [, m, d] = key.split('-');
  const md = `${m}-${d}`;
  const fixed = FIXED_GREGORIAN[md];
  if (fixed && !labels.includes(fixed)) labels.push(fixed);

  if (Number(m) === BIRTHDAY.month && Number(d) === BIRTHDAY.day) {
    labels.push(`${BIRTHDAY.recipient}生日`);
  }

  const unique = [...new Set(labels)];
  const primary = unique.find((l) => l !== solarTerm) || solarTerm || null;

  return {
    labels: unique,
    primary: primary ? (primary.length > 5 ? primary.slice(0, 5) : primary) : null,
    solarTerm,
    isBirthday: Number(m) === BIRTHDAY.month && Number(d) === BIRTHDAY.day,
  };
}

export function getDayDetailLines(key, dayType) {
  const marks = getDayMarks(key);
  const lines = [];
  if (marks.solarTerm) lines.push({ kind: 'solar', text: `节气 · ${marks.solarTerm}` });
  marks.labels.filter((l) => l !== marks.solarTerm).forEach((text) => {
    lines.push({ kind: 'festival', text });
  });
  if (dayType === 'holiday') lines.push({ kind: 'off', text: '法定节假日' });
  if (dayType === 'weekend') lines.push({ kind: 'weekend', text: '周末' });
  if (dayType === 'workday' && marks.labels.length === 0) {
    const dow = new Date(Number(key.slice(0, 4)), Number(key.slice(5, 7)) - 1, Number(key.slice(8, 10))).getDay();
    if (dow >= 1 && dow <= 5) lines.push({ kind: 'work', text: '工作日' });
  }
  return lines;
}

